"""Train-only RobustScaler utilities for P2 preprocessing."""

from __future__ import annotations

import json
import shutil
from dataclasses import dataclass
from pathlib import Path
from typing import Any

import joblib
import numpy as np
import pyarrow.parquet as pq
from sklearn.preprocessing import RobustScaler


@dataclass(frozen=True)
class ScalingStatus:
    """Result of the pre-scaling status heuristic."""

    status: str
    justification: str
    indicators: dict[str, Any]


@dataclass(frozen=True)
class RobustScalerFit:
    """Fitted RobustScaler plus feature quantile diagnostics."""

    scaler: RobustScaler
    feature_stats: dict[str, dict[str, float]]


def load_p1_feature_statistics(profile_path: Path) -> dict[str, dict[str, Any]]:
    """Load feature statistics generated by P1, if present."""

    with profile_path.open("r", encoding="utf-8") as file:
        profile = json.load(file)
    feature_stats = profile.get("feature_statistics", {})
    if not isinstance(feature_stats, dict):
        raise ValueError("P1 profile feature_statistics must be a JSON object")
    return feature_stats


def check_scaling_status(
    feature_stats: dict[str, dict[str, Any]], feature_names: list[str]
) -> ScalingStatus:
    """Classify the dataset as raw_unscaled or possibly_scaled.

    The heuristic intentionally stays conservative: a few already binary
    features are expected, but several large ranges or non-centered features
    indicate raw CIC-IoT features.
    """

    raw_indicators: list[str] = []
    centered_bounded = 0
    inspected = 0

    for feature in feature_names:
        stats = feature_stats.get(feature)
        if not isinstance(stats, dict):
            continue
        min_value = float(stats.get("min") or 0.0)
        max_value = float(stats.get("max") or 0.0)
        mean_value = float(stats.get("mean") or 0.0)
        std_value = float(stats.get("std") or 0.0)
        inspected += 1

        max_abs = max(abs(min_value), abs(max_value))
        if max_abs > 100.0 or abs(mean_value) > 10.0 or std_value > 10.0:
            raw_indicators.append(feature)
        if abs(mean_value) <= 1.0 and std_value <= 5.0 and max_abs <= 20.0:
            centered_bounded += 1

    if inspected == 0:
        return ScalingStatus(
            status="raw_unscaled",
            justification=(
                "No reusable P1 feature statistics were found; defaulting to scaling."
            ),
            indicators={
                "features_inspected": 0,
                "raw_indicator_features": [],
                "centered_bounded_features": 0,
            },
        )

    raw_ratio = len(raw_indicators) / inspected
    centered_ratio = centered_bounded / inspected
    if centered_ratio >= 0.75 and raw_ratio <= 0.10:
        status = "possibly_scaled"
        justification = (
            f"{centered_bounded}/{inspected} features are centered and bounded; "
            "double scaling should be skipped unless forced."
        )
    else:
        status = "raw_unscaled"
        justification = (
            f"{len(raw_indicators)}/{inspected} features have large ranges, means "
            "or standard deviations; RobustScaler train-only is required."
        )

    return ScalingStatus(
        status=status,
        justification=justification,
        indicators={
            "features_inspected": inspected,
            "raw_indicator_features": raw_indicators,
            "raw_indicator_count": len(raw_indicators),
            "raw_indicator_ratio": raw_ratio,
            "centered_bounded_features": centered_bounded,
            "centered_bounded_ratio": centered_ratio,
        },
    )


def fit_robust_scaler_from_parquet(
    train_parquet_path: Path, feature_names: list[str]
) -> RobustScalerFit:
    """Fit RobustScaler quantiles from train split only.

    Quantiles are computed column-by-column to avoid materializing the whole
    train matrix in memory.
    """

    centers = np.zeros(len(feature_names), dtype=np.float64)
    scales = np.ones(len(feature_names), dtype=np.float64)
    feature_stats: dict[str, dict[str, float]] = {}

    for idx, feature in enumerate(feature_names):
        table = pq.read_table(train_parquet_path, columns=[feature])
        values = table.column(0).combine_chunks().to_numpy(zero_copy_only=False)
        values = np.asarray(values, dtype=np.float64)
        q1, median, q3 = np.percentile(values, [25.0, 50.0, 75.0])
        scale = float(q3 - q1)
        if scale == 0.0:
            scale = 1.0
        centers[idx] = float(median)
        scales[idx] = scale
        feature_stats[feature] = {
            "q1": float(q1),
            "median": float(median),
            "q3": float(q3),
            "iqr": scale,
        }

    scaler = RobustScaler()
    scaler.center_ = centers
    scaler.scale_ = scales
    scaler.n_features_in_ = len(feature_names)
    scaler.feature_names_in_ = np.asarray(feature_names, dtype=object)
    return RobustScalerFit(scaler=scaler, feature_stats=feature_stats)


def save_scaler(scaler: RobustScaler, path: Path) -> None:
    """Persist a fitted scaler."""

    path.parent.mkdir(parents=True, exist_ok=True)
    joblib.dump(scaler, path)


def transform_parquet_to_npz(
    parquet_path: Path,
    output_path: Path,
    feature_names: list[str],
    target_column: str,
    target_output_name: str,
    *,
    scaler: RobustScaler | None,
    apply_scaling: bool,
    batch_size: int = 200_000,
) -> dict[str, Any]:
    """Transform one split parquet into an NPZ suitable for PyTorch datasets."""

    output_path.parent.mkdir(parents=True, exist_ok=True)
    parquet_file = pq.ParquetFile(parquet_path)
    n_rows = parquet_file.metadata.num_rows
    n_features = len(feature_names)

    temp_dir = output_path.parent / f".tmp_{output_path.stem}"
    if temp_dir.exists():
        shutil.rmtree(temp_dir)
    temp_dir.mkdir(parents=True, exist_ok=True)

    x_path = temp_dir / "X.npy"
    y_path = temp_dir / f"{target_output_name}.npy"
    label_path = temp_dir / "label_id_original.npy"
    row_path = temp_dir / "row_id.npy"

    x_data = np.lib.format.open_memmap(
        x_path, mode="w+", dtype=np.float32, shape=(n_rows, n_features)
    )
    y_data = np.lib.format.open_memmap(
        y_path, mode="w+", dtype=np.int16, shape=(n_rows,)
    )
    label_data = np.lib.format.open_memmap(
        label_path, mode="w+", dtype=np.int16, shape=(n_rows,)
    )
    row_data = np.lib.format.open_memmap(
        row_path, mode="w+", dtype=np.int64, shape=(n_rows,)
    )

    columns = ["row_id", *feature_names, "label_id", target_column]
    cursor = 0
    center = scaler.center_ if scaler is not None and apply_scaling else None
    scale = scaler.scale_ if scaler is not None and apply_scaling else None

    for batch in parquet_file.iter_batches(batch_size=batch_size, columns=columns):
        n_batch = batch.num_rows
        feature_matrix = np.empty((n_batch, n_features), dtype=np.float64)
        for col_idx, feature in enumerate(feature_names):
            values = batch.column(batch.schema.get_field_index(feature)).to_numpy(
                zero_copy_only=False
            )
            feature_matrix[:, col_idx] = np.asarray(values, dtype=np.float64)

        if center is not None and scale is not None:
            feature_matrix = (feature_matrix - center) / scale

        row_slice = slice(cursor, cursor + n_batch)
        x_data[row_slice] = feature_matrix.astype(np.float32, copy=False)
        y_data[row_slice] = np.asarray(
            batch.column(batch.schema.get_field_index(target_column)).to_numpy(
                zero_copy_only=False
            ),
            dtype=np.int16,
        )
        label_data[row_slice] = np.asarray(
            batch.column(batch.schema.get_field_index("label_id")).to_numpy(
                zero_copy_only=False
            ),
            dtype=np.int16,
        )
        row_data[row_slice] = np.asarray(
            batch.column(batch.schema.get_field_index("row_id")).to_numpy(
                zero_copy_only=False
            ),
            dtype=np.int64,
        )
        cursor += n_batch

    x_data.flush()
    y_data.flush()
    label_data.flush()
    row_data.flush()

    np.savez(
        output_path,
        X=x_data,
        **{
            target_output_name: y_data,
            "label_id_original": label_data,
            "row_id": row_data,
        },
    )
    shape_info = {
        "X": [int(n_rows), int(n_features)],
        target_output_name: [int(n_rows)],
        "label_id_original": [int(n_rows)],
        "row_id": [int(n_rows)],
    }

    del x_data
    del y_data
    del label_data
    del row_data
    shutil.rmtree(temp_dir)
    return {
        "path": output_path.as_posix(),
        "rows": int(n_rows),
        "feature_count": int(n_features),
        "arrays": shape_info,
        "size_bytes": output_path.stat().st_size,
    }
